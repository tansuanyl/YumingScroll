export function translateMessage(locale: Locale, source: string, values: TranslationValues = {}): string {
  const exact = locale === "en" ? enMessages[source] : undefined;
  const patternMatch = locale === "en" && !exact
    ? enMessagePatterns.find(({ pattern }) => pattern.test(source))
    : undefined;
  const template = exact || (patternMatch ? source.replace(patternMatch.pattern, patternMatch.replace) : source);
  return template.replace(/\{(\w+)\}/g, (match, key: string) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}

export function detectPreferredLocale(languages: readonly string[] = []): Locale {
  const preferred = languages.find(Boolean)?.toLowerCase() || "";
  return preferred.startsWith("zh") ? "zh-CN" : "en";
}

export function isLocale(value: string | null): value is Locale {
  return value === "zh-CN" || value === "en";
}
